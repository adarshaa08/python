"""
Functions in Python
A function is a block of code that only runs when it is called. You can pass data, known as parameters, into a function. A function can return data as a result.
"""
# Define a function
def greet(name):
    """Function to greet a person"""
    return f"Hello, {name}!"

# Call the function
print(greet("Alice"))
print(greet("Bob"))
